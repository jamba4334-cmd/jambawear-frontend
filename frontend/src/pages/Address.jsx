export default function Address() {
  const { user } = useAuth();
  const navigate = useNavigate();

  // Address State
  const [address, setAddress] = useState({
    name: '', phone: '', pincode: '', state: '', district: '', landmark: '', address1: '', address2: ''
  });
  const [originalAddress, setOriginalAddress] = useState(null);
  
  // UI States
  const [isEditing, setIsEditing] = useState(true);
  const [isSaving, setIsSaving] = useState(false);
  const [status, setStatus] = useState({ msg: '', type: '' });
  const [districts, setDistricts] = useState([]);

  // Load User Data
  useEffect(() => {
    async function fetchAddress() {
      if (user && user.uid) {
        try {
          const docRef = doc(db, "users", user.uid);
          const docSnap = await getDoc(docRef);
          if (docSnap.exists() && docSnap.data().address) {
            const savedData = docSnap.data().address;
            setAddress(savedData);
            setOriginalAddress(savedData);
            setIsEditing(false); // Default to view mode if data exists
            if (savedData.state) {
              setDistricts(indiaData[savedData.state] || []);
            }
          }
        } catch (error) {
          console.error("Error loading address:", error);
        }
      }
    }
    fetchAddress();
  }, [user]);

  // Handlers
  const handleChange = (e) => {
    const { id, value } = e.target;
    setAddress(prev => ({ ...prev, [id]: value }));

    if (id === 'state') {
      setDistricts(indiaData[value] || []);
      setAddress(prev => ({ ...prev, district: '' })); // Reset district when state changes
    }
  };

  const handleEdit = () => setIsEditing(true);

  const handleCancel = () => {
    if (originalAddress) {
      setAddress(originalAddress);
      setDistricts(indiaData[originalAddress.state] || []);
      setIsEditing(false);
    }
    setStatus({ msg: '', type: '' });
  };

  const handleSubmit = async (e) => {
    // Stop the page from reloading
    e.preventDefault(); 
    
    // Safety check: Is the user actually logged in?
    if (!user || !user.uid) {
      alert("Error: You must be logged in to save an address.");
      return;
    }

    // Manual Validation Check
    if (!address.name || !address.phone || !address.pincode || !address.state || !address.district || !address.landmark || !address.address1) {
      alert("Please fill out all required fields! (Name, Phone, Pincode, State, District, Landmark, and Address Line 1)");
      return;
    }

    setIsSaving(true);
    setStatus({ msg: 'Saving your address...', type: '' });

    try {
      // Pushing to Firebase
      await setDoc(doc(db, "users", user.uid), { address: address }, { merge: true });
      
      setOriginalAddress(address);
      setIsEditing(false);
      setStatus({ msg: 'Address saved! Redirecting to cart...', type: 'success' });
      
      // Redirect to cart
      setTimeout(() => {
        navigate('/cart');
      }, 800);

    } catch (error) {
      console.error("Error saving address:", error);
      setStatus({ msg: 'Failed to save. Please try again.', type: 'error' });
      alert("Database error: " + error.message);
    } finally {
      setIsSaving(false);
    }
  };

  return (
    <div className="address-container">
      <div className="page-header">
        <h1 className="page-title">Shipping Address</h1>
      </div>
      
      <div className="address-card">
        <p className="helper-text" style={{ color: isEditing ? '#d2b48c' : '#777' }}>
          {isEditing ? "Make your changes and click Save." : "Your saved delivery details. Click Edit to make changes."}
        </p>

        {/* Note the onSubmit={handleSubmit} here */}
        <form id="address-form" onSubmit={handleSubmit}>
          <div className="input-group">
            <label>Full Name</label>
            <input type="text" id="name" className="input-field" placeholder="Rahul Boro" disabled={!isEditing} value={address.name} onChange={handleChange} />
          </div>
          
          <div className="grid-2">
            <div className="input-group">
              <label>Phone Number</label>
              <input type="tel" id="phone" className="input-field" placeholder="10-digit mobile number" maxLength="10" disabled={!isEditing} value={address.phone} onChange={handleChange} />
            </div>
            <div className="input-group">
              <label>Pincode</label>
              <input type="text" id="pincode" className="input-field" placeholder="e.g. 781001" maxLength="6" disabled={!isEditing} value={address.pincode} onChange={handleChange} />
            </div>
          </div>

          <div className="grid-2">
            <div className="input-group">
              <label>State</label>
              <select id="state" className="input-field" disabled={!isEditing} value={address.state} onChange={handleChange}>
                <option value="" disabled>Select State</option>
                {Object.keys(indiaData).map(state => (
                  <option key={state} value={state}>{state}</option>
                ))}
              </select>
            </div>
            <div className="input-group">
              <label>District</label>
              <select id="district" className="input-field" disabled={!isEditing} value={address.district} onChange={handleChange}>
                <option value="" disabled>Select District</option>
                {districts.map(district => (
                  <option key={district} value={district}>{district}</option>
                ))}
              </select>
            </div>
          </div>

          <div className="input-group">
            <label>Landmark</label>
            <input type="text" id="landmark" className="input-field" placeholder="Village / Town / School" disabled={!isEditing} value={address.landmark} onChange={handleChange} />
          </div>

          <div className="input-group">
            <label>Address Line 1</label>
            <input type="text" id="address1" className="input-field" placeholder="House/Flat No., Building, Street Name" disabled={!isEditing} value={address.address1} onChange={handleChange} />
          </div>

          <div className="input-group">
            <label>Address Line 2</label>
            <input type="text" id="address2" className="input-field" placeholder="Area, Sector, Locality (Optional)" disabled={!isEditing} value={address.address2} onChange={handleChange} />
          </div>

          {!isEditing ? (
            <button type="button" className="btn edit-btn" onClick={handleEdit}>Edit Address</button>
          ) : (
            <div className="action-buttons">
              <button type="button" className="btn cancel-btn" onClick={handleCancel}>Cancel</button>
              {/* Note the type="submit" here */}
              <button type="submit" className="btn save-btn" disabled={isSaving}>
                {isSaving ? 'Saving...' : 'Save Changes'}
              </button>
            </div>
          )}

          {status.msg && (
            <div id="status-msg" className={status.type} style={{ display: 'block' }}>
              {status.msg}
            </div>
          )}
        </form>
      </div>
    </div>
  );
}